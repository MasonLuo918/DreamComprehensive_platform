package com.Dream.commons.bean;

public class ActivityResult {
    private Integer id;

    private String name;

    private String time;

    private String material;

    private String volunteer_time;

    private String activity_prove;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getVolunteer_time() {
        return volunteer_time;
    }

    public void setVolunteer_time(String volunteer_time) {
        this.volunteer_time = volunteer_time;
    }

    public String getActivity_prove() {
        return activity_prove;
    }

    public void setActivity_prove(String activity_prove) {
        this.activity_prove = activity_prove;
    }
}
